using ApiHomeworkDAL;
using ApiHomeworkDAL.Interfaces;
using ApiHomeworkDAL.Repositories;
using ApiHomeworkService.Interfaces;
using ApiHomeworkService.Services;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
builder.Services.AddDbContext<PersonalDbContext>();

builder.Services.AddTransient<IAddressRepo, AddressRepo>();
builder.Services.AddTransient<IAddressService, AddressService>();

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseAuthorization();

app.MapControllers();

app.Run();
